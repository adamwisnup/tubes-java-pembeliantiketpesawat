    public class HitungHargaEksekutif extends HitungHargaTiket implements HargaTiketCalculator {

    private final JComboBox<String> cbMaskapai;
    private final JComboBox<String> cbTujuan;
    private final JTextField formHargaTiket;
    private final JTextField formJumlahTiket;

    public HitungHargaEksekutif(int harga, int jumlahTiket, JComboBox<String> cbMaskapai, JComboBox<String> cbTujuan, JTextField formHargaTiket, JTextField formJumlahTiket) {
        super(harga, jumlahTiket);
        this.cbMaskapai = cbMaskapai;
        this.cbTujuan = cbTujuan;
        this.formHargaTiket = formHargaTiket;
        this.formJumlahTiket = formJumlahTiket;
    }
    
    @Override
    public int hitungTotalHarga() {
        String[] maskapaiOptions = { "CITILINK", "AIRASIA", "GARUDA" };
        String[] tujuanOptions = { "MINANG", "SOETA", "JUANDA" };

        int[][] hargaTiketMatrix = {
                { 1900000, 1500000, 1700000 },
                { 2300000, 1900000, 2100000 },
                { 2500000, 2100000, 2300000 }
        };

        String selectedMaskapai = cbMaskapai.getSelectedItem().toString();
        String selectedTujuan = cbTujuan.getSelectedItem().toString();

        hargaTiket = 0;

         // COnditional and looping
        for (int i = 0; i < maskapaiOptions.length; i++) {
            if (selectedMaskapai.equals(maskapaiOptions[i])) {
                for (int j = 0; j < tujuanOptions.length; j++) {
                    if (selectedTujuan.equals(tujuanOptions[j])) {
                        hargaTiket = hargaTiketMatrix[i][j];
                        break;
                    }
                }
                break;
            }
        }

        formHargaTiket.setText(String.valueOf(hargaTiket));

        // Call the hitungTotalHarga method from HitungHargaTiket
        return super.hitungTotalHarga();
    }
}
    
