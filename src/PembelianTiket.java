    private void btnPesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesanActionPerformed
        // TODO add your handling code here:
    pembelianBerhasil = true;
    pemesan = formNama.getText();
    maskapai = (String) cbMaskapai.getSelectedItem(); // Ambil item terpilih dari JComboBox
    tujuan = (String) cbTujuan.getSelectedItem(); // Ambil item terpilih dari JComboBox
    berangkat = "Bandara Internasional Husein Sastranegara";

    // Periksa radio button yang terpilih untuk mendapatkan tipe
    kelas = "";
    if (rbEkonomi.isSelected()) {
        kelas = "Ekonomi";
    } else if (rbBisnis.isSelected()) {
        kelas = "Bisnis";
    } else if (rbEksekutif.isSelected()) {
        kelas = "Eksekutif";
    }

    harga = formHargaTiket.getText();
    tiketDibeli = formJumlahTiket.getText();
    totalHargaTiketDibeli = formTotalHarga.getText();
       
    // Kirim nilai ke ResiTiket menggunakan konstruktor
    ResiTiket resiTiket = new ResiTiket(pemesan, maskapai, tujuan, kelas, harga, tiketDibeli, totalHargaTiketDibeli);
    
    // Menampilkan GUI ResiTiket
    resiTiket.pack();
    resiTiket.setVisible(true);
    resiTiket.setLocationRelativeTo(null);

    // Mengatur operasi penutupan jendela pada objek ResiTiket
    resiTiket.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE); 

    // Menutup jendela saat ini
    this.dispose();
    }//GEN-LAST:event_btnPesanActionPerformed

    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
    jumlahTiket = Integer.parseInt(formJumlahTiket.getText());

    // Initialize totalHarga variable
    totalHarga = 0;

    // Check the selected option (Bisnis or Eksekutif)
    if (rbBisnis.isSelected()) {
        // Calculate totalHarga for Bisnis
        HitungHargaBisnis hitungHargaBisnis = new HitungHargaBisnis(hargaTiket, jumlahTiket, cbMaskapai, cbTujuan, formHargaTiket, formJumlahTiket);
        
        if (jumlahTiket >= 3) {
            String promoCode = "Promo";
            totalHarga = hitungHargaBisnis.hitungTotalHarga(promoCode);
            } else {
            totalHarga = hitungHargaBisnis.hitungTotalHarga();
            }
//        totalHarga = hitungHargaBisnis.hitungTotalHarga();
    } else if (rbEksekutif.isSelected()) {
        // Calculate totalHarga for Eksekutif
        HitungHargaEksekutif hitungHargaEksekutif = new HitungHargaEksekutif(hargaTiket, jumlahTiket, cbMaskapai, cbTujuan, formHargaTiket, formJumlahTiket);
        
        if (jumlahTiket >= 2) {
            String promoCode = "Promo";
            totalHarga = hitungHargaEksekutif.hitungTotalHarga(promoCode);
            } else {
            totalHarga = hitungHargaEksekutif.hitungTotalHarga();
            }
        
//        totalHarga = hitungHargaEksekutif.hitungTotalHarga();
    }else if (rbEkonomi.isSelected()) {
        // Calculate totalHarga for Eksekutif
        HitungHargaEkonomi hitungHargaEkonomi = new HitungHargaEkonomi(hargaTiket, jumlahTiket, cbMaskapai, cbTujuan, formHargaTiket, formJumlahTiket);
        if (jumlahTiket >= 4) {
            String promoCode = "Promo"; 
            totalHarga = hitungHargaEkonomi.hitungTotalHarga(promoCode);
            } else {
            totalHarga = hitungHargaEkonomi.hitungTotalHarga();
            }
//        totalHarga = hitungHargaEkonomi.hitungTotalHarga();
    }

    // Display the totalHarga
    formTotalHarga.setText(String.valueOf(totalHarga));
    }//GEN-LAST:event_btnHitungActionPerformed

