//interface
interface HargaTiketCalculator {
    int hitungTotalHarga();
}

class HitungHargaTiket implements HargaTiketCalculator {
    public int harga;
    public int jumlahTiket;
    
    public HitungHargaTiket(int harga, int jumlahTiket){
        this.harga = harga;
        this.jumlahTiket = jumlahTiket;
    }
    
     // Overload 
    public int hitungTotalHarga(String promo) {
        int totalHarga = harga * jumlahTiket; 
        
        if ("Promo".equalsIgnoreCase(promo)) {
            double diskon = 0.1;
            totalHarga = (int) (totalHarga - (totalHarga * diskon));
        } 
        return totalHarga;
    }
//    override
    @Override
    public int hitungTotalHarga() {
        return harga * jumlahTiket;
    }
}

